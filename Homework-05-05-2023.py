# Це перший прямокутний трикутник.
n = int(input("Введiть кiлькiсть зiрочок: "))
m = 0
while n > 0:
    print(" " * m + "*" * n)
    n -= 1
    m += 1

# Це другий прямокутний трикутник.
n = int(input("Введiть кiлькiсть зiрочок: "))
m = 1
while n > 0:
    print(" " * (n - 1) + "*" * m)
    n -= 1
    m += 1

